// Scans demos/<Component>/basic.tsx and writes content/demos-index.ts — a
// client module that lazily (ssr:false) imports each demo, so canvas/WebGL
// demos never execute during SSR/prerender. Keyed by the registry slug.

import { readdirSync, existsSync, writeFileSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const appRoot = join(here, '..');
const demosDir = join(appRoot, 'demos');
const outFile = join(appRoot, 'content', 'demos-index.ts');

// Keep in sync with content/registry.ts toSlug.
const toSlug = (name) =>
  name
    .replace(/([a-z])([A-Z0-9])/g, '$1-$2')
    .replace(/([A-Z]+)([A-Z][a-z])/g, '$1-$2')
    .replace(/([0-9])([A-Za-z])/g, '$1-$2')
    .toLowerCase();

const dirs = existsSync(demosDir)
  ? readdirSync(demosDir, { withFileTypes: true })
      .filter(
        (d) => d.isDirectory() && existsSync(join(demosDir, d.name, 'basic.tsx')),
      )
      .map((d) => d.name)
      .sort()
  : [];

const body = [
  "'use client';",
  '',
  "import dynamic from 'next/dynamic';",
  "import { createElement, type ComponentType } from 'react';",
  '',
  '// AUTO-GENERATED by scripts/gen-demos-index.mjs — do not edit by hand.',
  '',
  '// Pulsing placeholder while a demo chunk loads (some, e.g. PhoneInput, pull',
  '// heavy deps) so the example area never looks blank or stuck.',
  'const DemoLoading = () =>',
  "  createElement('div', {",
  "    className: 'h-44px w-[280px] max-w-full animate-pulse rounded-12px bg-bg-tertiary',",
  '  });',
  '',
  'export const demoRegistry: Record<string, ComponentType> = {',
  ...dirs.map(
    (name) =>
      `  '${toSlug(name)}': dynamic(() => import('@/demos/${name}/basic'), { ssr: false, loading: DemoLoading }),`,
  ),
  '};',
  '',
];

writeFileSync(outFile, body.join('\n'));
console.log(`demos-index.ts: ${dirs.length} demo(s)`);
